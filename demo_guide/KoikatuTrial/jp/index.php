	<h4>　はじめに</h4>
<div id="page0Cont" class="ContBG">
	・<b>本プログラムについて</b><br>
	　この度は「コイカツ キャラメイク体験版」をダウンロードして頂き、誠にありがとう御座います。<br>
	　本プログラムは、弊社製品「コイカツ！」のキャラメイクシステムを体験するプログラムとなります。<br>
	<br>
	　※本プログラムで作成したキャラクターはゲーム本編に使用することが可能になる予定です。<br>
	<br>

・<b id="ope">基本操作</b><br>
　本プログラムでは以下のWindows基本操作を使用します。<br>
<br>
　<b>①左クリック</b> ： マウスの左ボタンを押して下さい。　　<b>②左ボタンドラッグ</b> ： マウスの左ボタンを押したままマウスを動かして下さい。<br>
	<br>
	　本マニュアルでは「～を選択して下さい」という表現がございます。<br>
	　こちらの記載があった場合は「～」の部分にマウスカーソルを移動させて、マウスの左ボタンをクリックして下さい。<br>
	　（例：「決定ボタンを選択して下さい」とありましたら、「決定ボタン」にマウスカーソルを移動させてマウスの左ボタンをクリックして下さい。）<br>
	<br>
	<b>
		　※場合によってはファイルのコピー・移動・削除等の操作を行う必要がある場合があります。<br>
		　　こちらがわからないという場合は、お手数ではございますがお使いのパソコンメーカーにお問い合わせ下さい。
	</b><br>
	<br>
	<b>　日本語入力</b> ： 入力欄で日本語入力をする場合は、入力欄をクリックしてキーボードの「Alt」キーを押しながら「半角／全角」キーを押してから文字を入力して下さい。<br>
	　※一度この操作を行うとゲームを再起動するか、再び同じ操作をするまで日本語入力になります。<br>
	<br>
	<b>　カメラ操作</b>：キャラメイクシーンでは下記操作でカメラの移動・回転が行えます。<br>
