	<h4>　インストール方法</h4>
<div id="instCont" class="ContBG">
	　①フォルダ内にあります「Startup.exe」を 右クリック → 管理者として実行 で起動して下さい。<br>
	<br>
	　②スタートアップの「コイカツ キャラメイク 体験版をインストール」を選択すると、インストーラーが起動しますので画面の指示に従いインストールを行って下さい。<br>
	　　※「C:\Program Files」等のシステムフォルダや、「D:\」等のバックアップ領域など、特定の場所にインストールすると各種データの保存が行えません。<br>
	　　　該当する場所にはインストールしないで下さい。<br>
	　　※インストール先の変更を行わずに、インストーラーが指定した場所へインストールする事を推奨致します。<br>
	　　※画面のメーターが進まない、応答なしの等の表記が出ても裏で作業が続いている可能性がありますので、お手数ではございますがお待ち頂けますようお願い致します。<br>
	<br>
	　③インストールが完了しますと、デスクトップに「コイカツ キャラメイク体験版」のアイコンが表示されます。<br>
	　　デスクトップにありますアイコンをダブルクリックで起動しますと「<a href="#startup">初期設定</a>」画面が表示されますので、こちらよりゲームを起動して下さい。<br>
	<br>
	　※一度インストールを行えば、再度インストールを行う必要はございません。<br>
	　　
</div>
	
	
